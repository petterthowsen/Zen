namespace Zen.Typing;

public enum ZenPrimitives {
    Object,       // Base type for all objects
    Integer,      // Integer value type
    Float,        // Floating-point value type
    Integer64,    // 64-bit integer value type
    Float64,      // 64-bit float value type
    Boolean,      // Boolean value type
    String,       // String value type
    Null,         // Special null type
    Void          // Void return type (typically used in function signatures)
}